#include <stdio.h>

int main() {
    int number = 10;                        // 'int' is a keyword used to declare an integer variable
    if (number > 0) {                       // 'if' is a keyword used to check if a condition is true 
        printf("Number is positive\n");     // 'printf' is a function from stdio.h
    }
    return 0;                               // 'return' is a keyword to return a value from a function
}


